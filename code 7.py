t.pendown()
t.left(90)
t.forward(100)
t.penup()
t.goto(100, 0)
t.pendown()
t.right(90)
t.backward(30)
t.left(90)
t.forward(100)
t.penup()
t.goto(40, 30)
t.pendown()
t.left(180)
t.forward(30)
t.right(90)
t.forward(30)
t.right(120)
t.backward(30)
t.penup()
t.goto(30, 50)
t.dot(4)
t.penup()
t.goto(10, 50)
t.dot(4)
t.penup()
t.goto(-50, 0)
t.pendown()
t.forward(20)
t.right(90)
t.forward(40)
t.left(30)
t.backward(60)
t.forward(20)
t.left(90)
t.backward(30)
t.right(90)
t.backward(50)
t.right(90)
t.backward(20)
t.penup()
t.goto(-30, -20)
t.dot(4)
t.penup()
t.goto(0, 0)

window.mainloop()